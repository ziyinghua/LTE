function[poutput]=Turbo_encoder(input,info_len)
poutput=zeros(3,info_len+4);
[parity1,tail_bits1]=rsc_encode(input,info_len);
alpha=interleave(info_len,input);
[parity2,tail_bits2]=rsc_encode(alpha,info_len);
poutput(1,1:info_len)=input;
poutput(2,1:info_len)=parity1;
poutput(3,1:info_len)=parity2;
poutput(1,info_len+1)=tail_bits1(1,1);
poutput(2,info_len+1)=tail_bits1(1,2);
poutput(3,info_len+1)=tail_bits1(1,3);
poutput(1,info_len+2)=tail_bits1(1,4);
poutput(2,info_len+2)=tail_bits1(1,5);
poutput(3,info_len+2)=tail_bits1(1,6);
poutput(1,info_len+3)=tail_bits2(1,1);
poutput(2,info_len+3)=tail_bits2(1,2);
poutput(3,info_len+3)=tail_bits2(1,3);
poutput(1,info_len+4)=tail_bits2(1,4);
poutput(2,info_len+4)=tail_bits2(1,5);
poutput(3,info_len+4)=tail_bits2(1,6);
end